def imgprocess(path):
    img = cv2.imread(path)
    img = cv2.resize(img,(100,32))
    img = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)/255.
    img = torch.tensor(img)
    img.sub_(0.5).div_(0.5)
    img = img.view(1,1,*img.size())
    if torch.cuda.is_available():
        img = img.cuda().type(torch.cuda.FloatTensor)
    return img



def crnn_pred(sess, image ,converter):
    
    image = image.cpu().detach().numpy()

    pred = torch.tensor(sess.run(None, {"input": image}))[0]
    _, preds = pred.max(2) # return max of value and it's index
    preds = preds.transpose(1, 0).contiguous().view(-1)
    preds_size = Variable(torch.IntTensor([preds.size(0)]))

    raw_pred = converter.decode(preds.data, preds_size.data, raw=True)
    sim_pred = converter.decode(preds.data, preds_size.data, raw=False)

    return raw_pred, sim_pred

